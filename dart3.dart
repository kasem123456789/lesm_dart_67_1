void main() {
  // Case Sensitive Example
  String name = "Alice";
  String _name = "BOb";
  String nAme = "Charli";

  print(name);    // Alice
  print(nAme);    // Charli
  print(_name);   // BOb

  print("-------");

  name = "David";
  int age = 20;
  double score = 95.5;
  num height = 147, weight = 70;
  bool isStudent = true;

  print(name);        // David
  print(age);         // 20
  print(score);       // 95.5
  print(height);      // 147
  print(weight);      // 70
  print(isStudent);   // true
}
